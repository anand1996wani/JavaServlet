package com.TestServlet;

public class ConflictMessages {
	private String sentNode;
	private String message;
	private String collabName;
	public String getSentNode() {
		return sentNode;
	}
	public void setSentNode(String sentNode) {
		this.sentNode = sentNode;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getCollabName() {
		return collabName;
	}
	public void setCollabName(String collabName) {
		this.collabName = collabName;
	}
}
